

for number in range(1,6):
    
    number = int(input("Enter mark : "))

    if number >= 75 :
        print("Your grade is A")
    elif number >= 65 :
        print("Your grade is B")
    elif number >= 50 :
        print("Your grade is C")
    elif number >= 35 :
        print("Your grade is S")   
    else:
        print("Your grade is F") 


